// 카테고리별 필터링 함수
window.getToysByCategory = function(category) {
    return window.toysDatabase.filter(toy => toy.category === category);
};

// 희귀도별 필터링 함수
window.getToysByRarity = function(minRarity) {
    return window.toysDatabase.filter(toy => toy.rarity >= minRarity);
};

// 연령별 필터링 함수
window.getToysForAge = function(age) {
    return window.toysDatabase.filter(toy => {
        const minAge = parseInt(toy.ageRange.match(/\d+/)[0]);
        return age >= minAge;
    });
};

// 랜덤 장난감 선택 함수
window.getRandomToy = function() {
    const randomIndex = Math.floor(Math.random() * window.toysDatabase.length);
    return window.toysDatabase[randomIndex];
};

// 희귀도에 따른 가중치 랜덤 선택
window.getWeightedRandomToy = function() {
    // 희귀도가 낮을수록 더 자주 나옴
    const weights = window.toysDatabase.map(toy => 11 - toy.rarity);
    const totalWeight = weights.reduce((a, b) => a + b, 0);
    
    let random = Math.random() * totalWeight;
    for (let i = 0; i < window.toysDatabase.length; i++) {
        random -= weights[i];
        if (random <= 0) {
            return window.toysDatabase[i];
        }
    }
    
    return window.toysDatabase[0];
};